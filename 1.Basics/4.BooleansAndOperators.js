// === equality operator
// !== not equal to operator
let temp = 31;
let isFreezing = temp === 31;
console.log(isFreezing);

// < less than operator
// > greater than operator
// <= less than equal to operator
// >= greater than equal to operator
let result = temp <= 32;
console.log(result);

